import type { Slot } from './rules'

export interface Layout {
  id: string
  name: string
  description: string
  slots: Slot[]
}

function layer(positions: number[][], z: number): Slot[] {
  return positions.map(([x, y]) => ({ x: x!, y: y!, z }))
}

