export const DEFAULT_LAYOUT_ID = 'garden'

export function getLayout(id: string): Layout {
  const layout = LAYOUTS.find((l) => l.id === id)
  if (!layout) throw new Error(`Unknown layout: ${id}`)
  return layout
}
